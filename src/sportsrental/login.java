    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
                   String nUsername = txtUsername.getText();
                    //Reading password as char array
                   char[] nPasswordChars = passwordField.getPassword();
                   //Convert the char array to a String
                   String nPassword = new String(nPasswordChars);
                   
                   sportsRentalDB rentalDB; rentalDB = new sportsRentalDB();
                   String password = rentalDB.getPasswordByUsername(nUsername);
                   
                    if (password == null) {
                    //Username not found in the database
                   lblMessage.setText("Username not found.");
                } else if (password.equals(nPassword)) {
                    //Password matches
                    System.out.println("Login successful.");
                    this.dispose();
                     WelcomePage wpage = new WelcomePage();
                     wpage.setVisible(true);
                    //Return to welcome page
                } else {
                    //Password does not match
                    lblMessage.setText("Incorrect password.");
                }
    }//GEN-LAST:event_btnLoginActionPerformed

